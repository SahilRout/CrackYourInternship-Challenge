class Solution
{
    public:
    //Function to find list of all words possible by pressing given numbers.
    map<int,string> mp;
    vector<string> ans;
    void solve(int i,int a[],int n,string temp)
    {
        if(i==n)
        {
            ans.push_back(temp);
            return;
        }
        for(int j=0;j<mp[a[i]].size();j++)
        {
            string s=mp[a[i]];
            solve(i+1,a,n,temp+s[j]);
        }
    }
    vector<string> possibleWords(int a[], int N)
    {
        mp[2]="abc";
        mp[3]="def";
        mp[4]="ghi";
        mp[5]="jkl";
        mp[6]="mno";
        mp[7]="pqrs";
        mp[8]="tuv";
        mp[9]="wxyz";
        solve(0,a,N,"");
        return ans;
    }
};


